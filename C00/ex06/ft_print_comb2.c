#include <unistd.h>

void	ft_print_comb2(void)
{
	char	comb2[5];
	int		a;
	int		b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			comb2[0] = '0' + a / 10;
			comb2[1] = '0' + a % 10;
			comb2[2] = ' ';
			comb2[3] = '0' + b / 10;
			comb2[4] = '0' + b % 10;
			write(1, &comb2, 5);
			if (a != 98)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
